package chapter2;

public class CharacterEx {

	public static void main(String[] args) {
		char ch = 'A';
		System.out.println(ch); // print 'A'
		
		char ch2 = 66; // char 자료형에 정수 입력
		System.out.println(ch2); // print 'B'
		
		int ch3 = 67;
		System.out.println(ch3); // print 67
		System.out.println((char)ch3); // print 'C'
	}

}
